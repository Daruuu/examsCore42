unsigned char	swap_bits(unsigned char octet)
{
	return ((octet >> 4) | (octet << 4));
}

#include <stdio.h>
int	main(int ac, char **av)
{
	if (ac == 2)
		printf("%d\n", swap_bits(atoi(av[1])));
	return (0);
}
